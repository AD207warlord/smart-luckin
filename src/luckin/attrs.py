def classify_attr(word: str) -> tuple[str, str] | None:
    """把用户输入的一个词归类到 (维度, 标准值)。

    解析顺序:
      1. 别名表精确匹配(用户口语)
      2. 各维度的值精确匹配(瑞幸标准枚举)
      3. 各维度的值包含匹配(如"冰"能匹配"少冰"——但歧义大,只返回首个)

    返回 None 表示无法归类(用户可改用 --size/--temp 等显式参数)。
    """
    if not word:
        return None
    w = word.strip()

    # 1. 别名
    if w in ALIASES:
        return ALIASES[w]

    # 2. 精确匹配维度值
    for dim, values in ATTR_VOCAB.items():
        if w in values:
            return (dim, w)

    # 3. 包含匹配(宽松,只取第一个命中)
    for dim, values in ATTR_VOCAB.items():
        for v in values:
            if w in v or v in w:
                return (dim, v)

    return None


def parse_spec_text(text: str) -> dict[str, str]:
    """把自由文本规格解析成 {维度: 标准值} 字典。

    支持:
      - 逗号/空格/斜杠分隔:"少冰,无糖,燕麦奶" / "少冰 无糖" / "少冰/无糖"
      - 别名归一:"冰的" → ("温度","冰")
      - 自动归类:"燕麦" → ("奶基","燕麦奶")

    无法归类的词跳过(返回的字典不含它),不报错——调用方决定是否提示。

    例:
      parse_spec_text("少冰,无糖,燕麦奶")
      → {"温度": "少冰", "糖度": "不另外加糖", "奶基": "燕麦奶"}
    """
    if not text:
        return {}

    # 分隔符:逗号/空格/斜杠/顿号
    import re
    words = [w.strip() for w in re.split(r"[,\s/、]+", text) if w.strip()]

    result: dict[str, str] = {}
    for w in words:
        classified = classify_attr(w)
        if classified:
            dim, val = classified
            result[dim] = val
    return result
